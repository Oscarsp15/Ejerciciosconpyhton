#Escribir un programa que almacene en una lista los siguientes precios, 50, 75, 46, 22, 80, 65, 8, 
# y muestre por pantalla el menor y el mayor de los precios.

precios = [50, 75, 46, 22, 80, 65, 8]

# Encontrar el menor y el mayor de los precios
precio_menor = min(precios)
precio_mayor = max(precios)

# Mostrar por pantalla el menor y el mayor de los precios
print(f"El menor precio es: {precio_menor}")
print(f"El mayor precio es: {precio_mayor}")
